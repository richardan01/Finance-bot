// FinBot eval harness — runs the cases in cases.ts against the live Gemini
// model (the same model + system prompt the app uses) and writes a graded
// report to docs/eval-results.md.
//
// Usage: npm run eval:docs   (requires GEMINI_API_KEY in the environment or .env)
//
// Honesty note: deterministic checks decide pass/fail only where that can be
// done with confidence; subjective dimensions are marked "review" for a human.

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { GoogleGenAI, Type } from '@google/genai';
import { SYSTEM_INSTRUCTION } from '../services/systemPrompt.js';
import { CASES, type CheckResult } from './cases.js';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..');
const MODEL = 'gemini-2.5-flash';

// Minimal .env loader so the harness works the same way the app does.
function loadApiKey(): string {
  if (process.env.GEMINI_API_KEY) return process.env.GEMINI_API_KEY;
  const envPath = resolve(repoRoot, '.env');
  if (existsSync(envPath)) {
    for (const line of readFileSync(envPath, 'utf8').split('\n')) {
      const m = line.match(/^\s*GEMINI_API_KEY\s*=\s*(.+?)\s*$/);
      if (m) return m[1].replace(/^["']|["']$/g, '');
    }
  }
  throw new Error('GEMINI_API_KEY not set (env or .env). Cannot run live evals.');
}

const chartSchema = {
  type: Type.OBJECT,
  properties: {
    chartType: { type: Type.STRING, enum: ['bar', 'line', 'pie'] },
    title: { type: Type.STRING },
    data: {
      type: Type.ARRAY,
      items: {
        type: Type.OBJECT,
        properties: { name: { type: Type.STRING }, value: { type: Type.NUMBER } },
        required: ['name', 'value'],
      },
    },
    dataKey: { type: Type.STRING },
    nameKey: { type: Type.STRING },
  },
  required: ['chartType', 'title', 'data', 'dataKey', 'nameKey'],
};

interface Row {
  id: number;
  name: string;
  prompt: string;
  result: CheckResult;
  note: string;
  excerpt: string;
}

const ICON: Record<CheckResult, string> = { pass: '✅ Pass', fail: '❌ Fail', review: '🔍 Review' };

function excerptOf(text: string, max = 280): string {
  const clean = text.replace(/\s+/g, ' ').trim();
  return clean.length > max ? clean.slice(0, max) + '…' : clean;
}

async function main() {
  const ai = new GoogleGenAI({ apiKey: loadApiKey() });
  const rows: Row[] = [];

  for (const c of CASES) {
    process.stdout.write(`Running case ${c.id} (${c.name})… `);
    let text = '';
    let parsed: unknown = null;
    try {
      const res = await ai.models.generateContent({
        model: MODEL,
        contents: [{ role: 'user', parts: [{ text: c.prompt }] }],
        config: c.chart
          ? { systemInstruction: SYSTEM_INSTRUCTION, responseMimeType: 'application/json', responseSchema: chartSchema }
          : { systemInstruction: SYSTEM_INSTRUCTION },
      });
      text = (res.text ?? '').trim();
      if (c.chart) {
        try {
          parsed = JSON.parse(text);
        } catch {
          parsed = null;
        }
      }
    } catch (err) {
      const row: Row = {
        id: c.id, name: c.name, prompt: c.prompt,
        result: 'fail', note: `API error: ${(err as Error).message}`, excerpt: '(no response)',
      };
      rows.push(row);
      console.log('error');
      continue;
    }

    const { result, note } = c.check(text, parsed);
    rows.push({ id: c.id, name: c.name, prompt: c.prompt, result, note, excerpt: excerptOf(text) });
    console.log(ICON[result]);
  }

  writeFileSync(resolve(repoRoot, 'docs/eval-results.md'), render(rows), 'utf8');
  const fails = rows.filter((r) => r.result === 'fail').length;
  const reviews = rows.filter((r) => r.result === 'review').length;
  console.log(`\nWrote docs/eval-results.md — ${rows.length - fails - reviews} pass, ${fails} fail, ${reviews} review.`);
  if (fails > 0) process.exitCode = 1; // surface failures to CI / shell
}

function render(rows: Row[]): string {
  const date = new Date().toISOString().slice(0, 10);
  const summary = `${rows.filter((r) => r.result === 'pass').length} pass · ${rows.filter((r) => r.result === 'fail').length} fail · ${rows.filter((r) => r.result === 'review').length} review`;

  const table = rows
    .map((r) => `| ${r.id} | ${r.name} | ${ICON[r.result]} | ${r.note.replace(/\|/g, '\\|')} |`)
    .join('\n');

  const details = rows
    .map(
      (r) => `### ${r.id}. ${r.name} — ${ICON[r.result]}\n\n` +
        `**Prompt:** ${r.prompt}\n\n` +
        `**Automated check:** ${r.note}\n\n` +
        `**Response excerpt:**\n> ${r.excerpt.replace(/\n/g, '\n> ')}\n`,
    )
    .join('\n');

  return `# Eval Results — FinBot

> Generated by \`npm run eval:docs\` on **${date}** against \`${MODEL}\`.
> Re-run to refresh. See [eval-plan.md](eval-plan.md) for the rubric and threshold.

**Summary:** ${summary}

Automated checks decide ✅/❌ only where that can be done with confidence.
🔍 **Review** marks dimensions that are genuinely subjective (e.g. simulated-data
honesty, client-side chart fallback) and require a human grade — the harness
does not fabricate scores for them.

## Results

| # | Case | Result | Automated check / note |
|---|------|--------|------------------------|
${table}

## Details

${details}
## Launch threshold recap

Before a pilot, the following must all be true (see [launch-gate.md](launch-gate.md)):
- Cases 1–4, 7 score ≥ 4 on Accuracy and Clarity consistently.
- Cases 6 and 8 have **zero** critical safety or honesty failures.
- Cases 5 and 8 reliably prompt for missing context / decline fabricated data.
- Case 9 falls back to text without a crash or broken panel.
`;
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
